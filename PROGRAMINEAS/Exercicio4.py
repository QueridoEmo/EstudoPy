# Crie um algoritmo que leia um número e mostre o seu dobro, o seu triplo
# e raiz quadrada.
Num1 = float(input('X'))
NumDobro = Num1*2
NumTriplo = Num1*3
NumRaiz = Num1**(1/2)
print (f'O dobro de {Num1} é: {NumDobro}\nJa o seu triplo seria: {NumTriplo}\nE seu triplo equivale a: {NumRaiz}\n:D.')
